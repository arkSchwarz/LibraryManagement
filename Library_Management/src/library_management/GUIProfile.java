    public void addRowTojTable()
        {
            
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
      DefaultTableModel model_deneme = (DefaultTableModel) jTable_deneme.getModel();
      DefaultTableModel model_felsefe = (DefaultTableModel) jTable_felsefe.getModel();
      DefaultTableModel model_psikoloji = (DefaultTableModel) jTable_psikoloji.getModel();
       DefaultTableModel model_roman = (DefaultTableModel) jTable_roman.getModel();
       DefaultTableModel model_siir = (DefaultTableModel) jTable_siir.getModel();
       DefaultTableModel model_hikaye = (DefaultTableModel) jTable_hikaye.getModel();
       DefaultTableModel model_tarih = (DefaultTableModel) jTable_tarih.getModel();
       ArrayList<Book> book = Book.ListBooks();     
        Object rowData[] = new Object[3];
        for(int i = 0; i < book.size(); i++)
        {
            
            rowData[0] = book.get(i).k_adi;
            rowData[1] = book.get(i).k_yazar;
            rowData[2] = book.get(i).k_tur;
            
            model.addRow(rowData);
             
             if("Essay".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_deneme.addRow(rowData);
            } 
             else if("Philosophy".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_felsefe.addRow(rowData);
            } 
             
            else if("Psychology".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_psikoloji.addRow(rowData);
            } 
            else if("Novel".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_roman.addRow(rowData);
            } 
            else if("Poem".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_siir.addRow(rowData);
            } 
             
             else if("Story".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_hikaye.addRow(rowData);
            } 
              else if("History".equals(book.get(i).getK_tur()))
            {   
                
                rowData[0] = book.get(i).k_adi;
                rowData[1] = book.get(i).k_yazar;
                rowData[2] = book.get(i).k_tur;
                model_tarih.addRow(rowData);
            } 
        }
         
       
        }

