    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
    
       User b= testUser.searchUser(Integer.parseInt(signin_name.getText()));
        DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();

        DefaultTableModel model2 = (DefaultTableModel) jTable7.getModel();

        int[] indexs = jTable7.getSelectedRows();

        Object[] row = new Object[3];

        for(int i = 0; i < indexs.length; i++)
        {
            row[0] = model2.getValueAt(indexs[i], 0);
            row[1] = model2.getValueAt(indexs[i], 1);
            row[2] = model2.getValueAt(indexs[i], 2);

            model1.addRow(row);

        }
        
       
        
      
         for(int l=0; l<Book.borrowBook.size(); l++)
         {
             if(((String) row[0]).equals(Book.borrowBook.get(l).getK_adi()))
             {
                 Book.borrowBook.remove(Book.borrowBook.get(l));
             }
         }
        
        
         Book isExist = Book.searchBook((String)row[0]);
        if(isExist != null){
            Book.show_b.remove(isExist);
            
        }
        model2.removeRow(jTable7.getSelectedRow());
       
        
       
        
        
       
        
    if("Standart".equals(b.getPaketIsmi())&& model2.getRowCount()<b.getMaxKitapCount())
      {
           
              jButton_borrow.setEnabled(true);
              
              
      }
      else if("Silver".equals(b.getPaketIsmi())&& model2.getRowCount()<b.getMaxKitapCount())
      {jButton_borrow.setEnabled(true);
      }
      else if("Gold".equals(b.getPaketIsmi())&& model2.getRowCount()<b.getMaxKitapCount())
      {jButton_borrow.setEnabled(true);
      }
      else if("Platnium".equals(b.getPaketIsmi())&& model2.getRowCount()<b.getMaxKitapCount())
      {
          jButton_borrow.setEnabled(true);
      }
        b.decreaseKitapCount();
   
    }//GEN-LAST:event_jButton_addActionPerformed

